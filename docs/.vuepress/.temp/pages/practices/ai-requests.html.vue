<h4 id="🔍-原理解析" tabindex="-1"><a class="header-anchor" href="#🔍-原理解析"><span>🔍 原理解析</span></a></h4>
<p><strong>核心原理</strong>：Cursor的1个Request最多可以包含25次Tool Calls，通过巧妙的会话续接技术，实现更长的对话链条。</p>
